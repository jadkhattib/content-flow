// Helper function to get the appropriate icon for a platform
export const getPlatformIcon = (platform: string, className?: string) => {
  const platformLower = platform.toLowerCase()
  
  if (platformLower.includes('twitter') || platformLower.includes('x')) {
    return <TwitterIcon className={className} />
  }
  if (platformLower.includes('facebook')) {
    return <FacebookIcon className={className} />
  }
  if (platformLower.includes('instagram')) {
    return <InstagramIcon className={className} />
  }
  if (platformLower.includes('tiktok')) {
    return <TikTokIcon className={className} />
  }
  if (platformLower.includes('youtube')) {
    return <YouTubeIcon className={className} />
  }
  if (platformLower.includes('linkedin')) {
    return <LinkedInIcon className={className} />
  }
  if (platformLower.includes('reddit')) {
    return <RedditIcon className={className} />
  }
  if (platformLower.includes('pinterest')) {
    return <PinterestIcon className={className} />
  }
  if (platformLower.includes('news')) {
    return <NewsIcon className={className} />
  }
  if (platformLower.includes('blog')) {
    return <BlogsIcon className={className} />
  }
  if (platformLower.includes('forum')) {
    return <ForumsIcon className={className} />
  }
  
  // Default fallback
  return (
    <svg className={className} viewBox="0 0 24 24" fill="currentColor">
      <path d="M12 2C6.48 2 2 6.48 2 12s4.48 10 10 10 10-4.48 10-10S17.52 2 12 2zm-2 15l-5-5 1.41-1.41L10 14.17l7.59-7.59L19 8l-9 9z"/>
    </svg>
  )
} 